    private void ApplyListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_ApplyListValueChanged

        schloarshipname = (String)ApplyList.getSelectedValue();

        try{

            conn = DriverManager.getConnection("jdbc:derby://localhost:1527/rose;create=true");
            if(conn != null){
                psInsert = conn.prepareStatement("SELECT * FROM SCHOLARSHIP WHERE NAME = ?");
                psInsert.setString(1,schloarshipname);
                resultSet = psInsert.executeQuery();

                while (resultSet.next()) {
                    name.setText(resultSet.getString("NAME"));
                    information.setText(resultSet.getString("Information"));
                    program.setText(resultSet.getString("PROGRAM"));

                }

                s.close();
                resultSet.close();
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_ApplyListValueChanged

